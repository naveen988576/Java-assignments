package naveen.java;

public class Variables_Example1 {

	public static void main(String[] args) {
		int number1=12,number2=13,number3=11,avg,sum,sub,mul;
		sum=number1+number2+number3;
		sub=number1-number2-number3;
		mul=number1*number2*number3;
		avg=number1+number2+number3/3;
		System.out.println("sum of three numbers is :"+sum);
		System.out.println("sub of three numbers is :"+sub);
		System.out.println("mul of three numbers is :"+mul);
		System.out.println("avg of three numbers is :"+avg);
	}

}
